package MainPackage;

public class Item {
    String name;
    String serialNumber;
    double value;

    public String getName() {
        //gets the name of the current item
        return name;
    }

    public String getSerialNumber() {
        //gets the id of the current item
        return serialNumber;
    }

    public double getValue() {
        //gets the value of the current item
        return value;
    }
}
